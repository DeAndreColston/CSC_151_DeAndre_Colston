package labs.example.loops;

public class WhileLoop {

    public static void main(String[] args) {
        executeWhileLoop();
    }
    private static void executeWhileLoop() { 
        int number = 0;
        
        while (number = 10)
        {
            number++;
        }
            System.out.println("The loop executed " + number + "times");
            return;
    }
}